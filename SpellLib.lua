-- checks if a table contains a certain item
-- @param _table the table to search
-- @param _item the item  value
function SpellLib:tContains(_table, _item)
    local index = 1;
    while _table[index] do
        if ( _item == _table[index] ) then        
            return 1;
        end
        index = index + 1;
    end
    return nil;
