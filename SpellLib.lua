-- returns a table with all spells for a certain class and spec
-- @param _class the identifier for the class
-- @param _sepc the identifier for the specilization
-- @param _includeBasic boolean indicates if the basic class spells will be returned aswell
-- @param _includeCovenant boolean indicates if the covenant class spells will be returned aswell
function SpellLib:GetClassSpecSpells(_class, _spec, _includeBasic, _includeCovenant)
    _spec = _spec or "ALL"
    _includeBasic = _includeBasic or true
    _includeCovenant = _includeCovenant or true
    
    local getSpells = {}                                                                               
    if _class == nil or _class == "" then
        print(self.errorText .. 'Error in SpellLib:GetClassCovenantSpells() first parameter (_class) is undefined.')
        return nil
    elseif self:tContains(self.classNames, _class) ~= 1 then
        print(self.errorText .. 'Error in SpellLib:GetClassCovenantSpells() first parameter (_class) set to an illegal value.')
        return nil
    elseif _class == "ALL" then
        return self.classSpells[_class][_spec]
    elseif _spec == nil or _spec == "" then 
        print(self.errorText .. 'Error in SpellLib:GetClassSpecSpells() second parameter (_spec) is undefined.')
        return nil         
    elseif self:tContains(self.specNames, _spec) ~= 1 then
        print(self.errorText .. 'Error in SpellLib:GetClassCovenantSpells() second parameter (_spec) set to an illegal value.')
        return nil
    else        
        if _spec == "ALL" then
            return self.classSpells[_class]
        else
            getSpells[_spec] = self.classSpells[_class][_spec]
            if _includeBasic == true then
                getSpells['BASIC'] = {}
                for i,v in pairs(self.classSpells[_class]['BASIC']) do
                    tinsert(getSpells['BASIC'], v)
                end                 
            end            
            if _includeCovenant == true then 
                getSpells['COVENANT'] = {}
                for i,v in pairs(self.classSpells[_class]['COVENANT']) do
                    tinsert(getSpells['COVENANT'], v)
                end                            
            end
        end     
    end
    return getSpells                    
end

